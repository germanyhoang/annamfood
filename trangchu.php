    function updateCountdown() {
        // Lấy thời gian hiện tại
        var now = new Date().getTime();

        // Tính toán thời gian cuối cùng của ngày
        var endOfDay = new Date();
        endOfDay.setHours(23, 59, 59, 999);
        var endOfDayTime = endOfDay.getTime();

        // Tính toán thời gian còn lại trong ngày
        var timeLeft = endOfDayTime - now;

        // Cập nhật nội dung của phần tử HTML
        var countdownElement = document.getElementById('countdown');
        var countdownElement1 = document.getElementById('countdown1');
        var countdownElement2 = document.getElementById('countdown2');
        var countdownElement3 = document.getElementById('countdown3');
        // Nếu thời gian còn lại hết, tính toán lại thời gian còn lại đến cuối ngày tiếp theo
        if (timeLeft < 0) {
            var tomorrow = new Date();
            tomorrow.setDate(tomorrow.getDate() + 1);
            tomorrow.setHours(0, 0, 0, 0);
            var tomorrowTime = tomorrow.getTime();
            timeLeft = tomorrowTime - now;
        }

        // Tính toán giờ, phút, giây
        var secondsLeft = Math.floor(timeLeft / 1000);
        var hours = Math.floor(secondsLeft / 3600);
        var minutes = Math.floor((secondsLeft % 3600) / 60);
        var seconds = secondsLeft % 60;

        // Hiển thị thời gian còn lại trong phần tử HTML
        countdownElement.innerHTML = hours + ' giờ ' + '-' + ' ' + ' ' + minutes + ' phút ' + '-' + ' ' + ' ' + seconds + ' giây';
        countdownElement1.innerHTML = hours + ' giờ ' + '-' + ' ' + ' ' + minutes + ' phút ' + '-' + ' ' + ' ' + seconds + ' giây';
        countdownElement2.innerHTML = hours + ' giờ ' + '-' + ' ' + ' ' + minutes + ' phút ' + '-' + ' ' + ' ' + seconds + ' giây';
        countdownElement3.innerHTML = hours + ' giờ ' + '-' + ' ' + ' ' + minutes + ' phút ' + '-' + ' ' + ' ' + seconds + ' giây';
    }

    // Cập nhật thời gian còn lại mỗi giây
    updateCountdown();
    var intervalId = setInterval(updateCountdown, 1000);
